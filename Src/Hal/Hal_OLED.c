// reverse display mode, 0:normal, 1:reverse
void hal_Oled_Color_Turn(unsigned char i)
{
	if(i==0)
		{
			hal_Oled_WR_Byte(0xA6,OLED_CMD);	// normal
		}
	if(i==1)
		{
			hal_Oled_WR_Byte(0xA7,OLED_CMD);	// reverse
		}
}


void hal_Oled_Display_Turn(unsigned char i)
{
	if(i==0)
		{
			hal_Oled_WR_Byte(0xC8,OLED_CMD);
			hal_Oled_WR_Byte(0xA1,OLED_CMD);
		}
	if(i==1)
		{
			hal_Oled_WR_Byte(0xC0,OLED_CMD);
			hal_Oled_WR_Byte(0xA0,OLED_CMD);
		}
}

static void hal_Oled_WR_Byte(unsigned char dat,unsigned char cmd)
{		
	unsigned char retry=0;		
	if(cmd)
	{
		OLED_DC_Set();
	}else 
	{
		OLED_DC_Clr();		  
	}
	 
	SPI1->DR=dat;	 	  
			 
	retry=0;
 
	while((SPI1->SR & SPI_I2S_FLAG_BSY) != RESET)
	{
		retry++;
		if(retry>200)
			return;
	}	  						    
 		 		  
	OLED_DC_Set();   	  
}


void hal_Oled_Display_on(void)
{
	hal_Oled_WR_Byte(0x8D,OLED_CMD);
	hal_Oled_WR_Byte(0x14,OLED_CMD);
	hal_Oled_WR_Byte(0xAF,OLED_CMD);
}


void hal_Oled_Display_Off(void)
{
	hal_Oled_WR_Byte(0x8D,OLED_CMD);
	hal_Oled_WR_Byte(0x10,OLED_CMD);
	hal_Oled_WR_Byte(0xAE,OLED_CMD);
}


void hal_Oled_Refresh(void)
{
	unsigned char i,n;
	for(i=0;i<8;i++)
	{
	   hal_Oled_WR_Byte(0xb0+i,OLED_CMD); // set startubg address
	   hal_Oled_WR_Byte(0x00,OLED_CMD);   // set low column start address
	   hal_Oled_WR_Byte(0x10,OLED_CMD);   // set high column start address
	   for(n=0;n<128;n++)
		 hal_Oled_WR_Byte(OLED_GRAM[n][i],OLED_DATA);
  }
}

void hal_Oled_Clear(void)
{
	unsigned char i,n;
	for(i=0;i<8;i++)
	{
	   for(n=0;n<128;n++)
		{
		 OLED_GRAM[n][i]=0;
		}
	}
	hal_Oled_Refresh();
}


//x:0~127
//y:0~63
//t:1->fill 0->clear	
static void hal_Oled_DrawPoint(unsigned char x,unsigned char y,unsigned char t)
{
	unsigned char i,m,n;
	i=y/8;
	m=y%8;
	n=1<<m;
	if(t){OLED_GRAM[x][i]|=n;}
	else
	{
		OLED_GRAM[x][i]=~OLED_GRAM[x][i];
		OLED_GRAM[x][i]|=n;
		OLED_GRAM[x][i]=~OLED_GRAM[x][i];
	}
}


//x1,y1: start coordinate
//x2,y2: end coordinate
void hal_Oled_DrawLine(unsigned char x1,unsigned char y1,unsigned char x2,unsigned char y2,unsigned char mode)
{
	unsigned short t; 
	int xerr=0,yerr=0,delta_x,delta_y,distance;
	int incx,incy,uRow,uCol;
	delta_x=x2-x1; 
	delta_y=y2-y1;
	uRow=x1;
	uCol=y1;
	if(delta_x>0)
	{
		incx=1; 
	}else if(delta_x==0)
	{
		incx=0;
	}else 
	{
		incx=-1;
		delta_x=-delta_x;
	}
	if(delta_y>0)
	{
		incy=1;
	}else if(delta_y==0)
	{
		incy=0;
	}else 
	{
		incy=-1;
		delta_y=-delta_x;
	}
	if(delta_x>delta_y)
	{
		distance=delta_x; 
	}else 
	{
		distance=delta_y;
	}
	for(t=0;t<distance+1;t++)
	{
		hal_Oled_DrawPoint(uRow,uCol,mode);
		xerr+=delta_x;
		yerr+=delta_y;
		if(xerr>distance)
		{
			xerr-=distance;
			uRow+=incx;
		}
		if(yerr>distance)
		{
			yerr-=distance;
			uCol+=incy;
		}
	}
}

//x,y: circle center coor
//r: radius
void hal_Oled_DrawCircle(unsigned char x,unsigned char y,unsigned char r)
{
	int a, b,num;
    a = 0;
    b = r;
    while(2 * b * b >= r * r)      
    {
        hal_Oled_DrawPoint(x + a, y - b,1);
        hal_Oled_DrawPoint(x - a, y - b,1);
        hal_Oled_DrawPoint(x - a, y + b,1);
        hal_Oled_DrawPoint(x + a, y + b,1);
 
        hal_Oled_DrawPoint(x + b, y + a,1);
        hal_Oled_DrawPoint(x + b, y - a,1);
        hal_Oled_DrawPoint(x - b, y - a,1);
        hal_Oled_DrawPoint(x - b, y + a,1);
        
        a++;
        num = (a * a + b * b) - r*r;
        if(num > 0)
        {
            b--;
            a--;
        }
    }
}




//x:0~127
//y:0~63
//size1:font 6x8/6x12/8x16/12x24
//mode:0->reverse display; 1->normal display
void hal_Oled_ShowChar(unsigned char x,unsigned char y,unsigned char chr,unsigned char size1,unsigned char mode)
{
	unsigned char i,m,temp,size2,chr1;
	unsigned char x0=x,y0=y;
	if(size1==8)
	{
		size2=6;
	}else 
	{
		size2=(size1/8+((size1%8)?1:0))*(size1/2);  
	}
	chr1=chr-' '; 
	for(i=0;i<size2;i++)
	{
		if(size1==8)
		{
			temp=asc2_0806[chr1][i];	// 0806
		}else if(size1==12)
        {
			temp=asc2_1206[chr1][i];	// 1206
		}else if(size1==16)
        {
			temp=asc2_1608[chr1][i];	// 1608
		}else if(size1==24)
        { 
			temp=asc2_2412[chr1][i];	// 2412
		} 
		else 
		{
			return;
		}
		for(m=0;m<8;m++)
		{
			if(temp&0x01)
			{
				hal_Oled_DrawPoint(x,y,mode);
			}else 
			{
				hal_Oled_DrawPoint(x,y,!mode);
			}
			temp>>=1;
			y++;	
		
		}
		x++;		
		if((size1!=8)&&((x-x0)==size1/2))	
		{
			x=x0;y0=y0+8;
		}
		y=y0;
  }
}


//x,y:   starting coordinate 
//size1: font size
//*chr:  string address 
//mode:  0->reverse display; 1->normal display
void hal_Oled_ShowString(unsigned char x,unsigned char y,unsigned char *chr,unsigned char size1,unsigned char mode)
{
	while((*chr>=' ')&&(*chr<='~'))	// invalid char
	{
		hal_Oled_ShowChar(x,y,*chr,size1,mode);
		if(size1==8)
		{
			x+=6;
		}
		else 
		{	
			x+=size1/2;
		}
		chr++;
  }
}

//m^n
static unsigned int hal_Oled_pow(unsigned char m,unsigned char n)
{
	unsigned int result=1;
	while(n--)
	{
	  result*=m;
	}
	return result;
}


//x,y : starting coordinate
//num : number
//len : length of number
//size: font size
//mode:	0->reverse display; 1->normal display
void hal_Oled_ShowNum(unsigned char x,unsigned char y,unsigned int num,unsigned char len,unsigned char size1,unsigned char mode)
{
	unsigned char t,temp,m=0;
	if(size1==8)
	{
		m=2;
	}
	for(t=0;t<len;t++)
	{
		temp=(num/hal_Oled_pow(10,len-t-1))%10;
		if(temp==0)
		{
			hal_Oled_ShowChar(x+(size1/2+m)*t,y,'0',size1,mode);
		}else 
		{
		  hal_Oled_ShowChar(x+(size1/2+m)*t,y,temp+'0',size1,mode);
		}
  }
}



// x, y: starting coordinate
// sizex, sizey: picture size
// BMP[]: bitmap array
// mode: 0->reverse display; 1->normal display
void hal_Oled_ShowPicture(unsigned char x, unsigned char y, unsigned char sizex, unsigned char sizey, const unsigned char BMP[], unsigned char mode)
{
    unsigned short j = 0;        	
    unsigned char i, n, temp, m; 	
    unsigned char x0 = x; 			
        
    sizex = sizex / 8; 	
        
    for (n = 0; n < sizey; n++)
    {
        for (i = 0; i < sizex; i++)
        {
            temp = BMP[j]; 
            j++; 			

            for (m = 0; m < 8; m++)
            {
                if (temp & 0x80) 
                {
                    hal_Oled_DrawPoint(x, y, mode); 
                } 
                else 
                {
                    hal_Oled_DrawPoint(x, y, !mode); 
                }
                temp <<= 1; 	
                x++; 			
            }
        }

        x = x0; 	
        y = y + 1; 	
    }
}


