	hal_Oled_WR_Byte(0x80,OLED_CMD);//--set divide ratio, Set Clock as 100 Frames/Sec
	hal_Oled_WR_Byte(0xD9,OLED_CMD);//--set pre-charge period
	hal_Oled_WR_Byte(0xF1,OLED_CMD);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
